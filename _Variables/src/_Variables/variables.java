package _Variables;

public class variables {

	public static void main(String[] args) {
		//VARIABLES TIPO ENTERO
		
		int numero0 = 0;
		int numero1 = 1;
		int numero2 = 2;
		System.out.println(numero0);
		System.out.println(numero1);
		System.out.println(numero2);
			
		numero0 = 4;
		numero1 = 5;
		numero2 = 6;	
		System.out.println(numero0);
		System.out.println(numero1);
		System.out.println(numero2);
			
	//VARIABLES TIPO BOOLEANO
		
		boolean variableVerdadero = true;
		boolean variableFalso = false;
		boolean variableVF = true;
		System.out.println(variableVerdadero);
		System.out.println(variableFalso);
		System.out.println(variableVF);
			
		variableVerdadero = false;
		variableFalso = true;
		variableVF = false;
		System.out.println(variableVerdadero);
		System.out.println(variableFalso);
		System.out.println(variableVF);
	
	//VARIABLES TIPO DOUBLE
		
		double decimalX = 3.142387492;
		double decimalY = 23.232304874;
		double decimalZ = 567.192983742;
		System.out.println(decimalX);
		System.out.println(decimalY);
		System.out.println(decimalZ);
		
		decimalX = 6.2839844;
		decimalY = 46.4634934;
		decimalZ = 1143.384384;
		System.out.println(decimalX);
		System.out.println(decimalY);
		System.out.println(decimalZ);
									
	//VARIABLES TIPO LONG
		
		long largo1 = 41312365765L;
		long largo2 = 59379814798L;
		long largo3 = 83461745134L;
		System.out.println(largo1);
		System.out.println(largo2);
		System.out.println(largo3);
		
		largo1 = 63461745134L;
		largo2 = 99379814798L;
		largo3 = 74395809348L;
		System.out.println(largo1);
		System.out.println(largo2);
		System.out.println(largo3);
				
	//VARIABLES TIPO CHAR
		
		char vocal1 = 'a';
		char vocal2 = 'b';
		char vocal3 = 'c';
		System.out.println(vocal1);
		System.out.println(vocal2);
		System.out.println(vocal3);
		
		vocal1 = 'x';
		vocal2 = 'y';
		vocal3 = 'z';
		System.out.println(vocal1);
		System.out.println(vocal2);
		System.out.println(vocal3);
				
	//VARIABLES TIPO STRING
		
		String nombre = "Julio Cesar";
		String apellidos = "Bequer de Vega";
		String aficiones = "me gusta tocar la guitarra";
		System.out.println(nombre);
		System.out.println(apellidos);
		System.out.println(aficiones);
		
		nombre = "Han Solo";
		apellidos = "Lee Jones";
		aficiones = "prefiero la bateria";
		System.out.println(nombre);
		System.out.println(apellidos);
		System.out.println(aficiones);
				
	}

}

	


