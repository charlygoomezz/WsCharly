package _EjercicioDoWhile;

public class _Ejercicio2 {
	//IMPRIMIR LOS NÚMEROS DEL 10 AL 1 EN ORDEN DESCENDENTE

	public static void main(String[] args) {
		System.out.println("APLICACIÓN ORDEN DESCENDENTE");
		System.out.println("----------------------------");
		System.out.println("----------------------------");
		
		int i = 10;
		do {
			System.out.println(i--);
			
		}while(i >= 1);

	}

}

