package _Ejercicios;

public class Interes_Simple {

	public static void main(String[] args) {
		
		double principal;
		principal = 1_000_000;
		double numero;
		numero = 5;
		double porcentaje;
		porcentaje = numero / 100;
		double tasa = porcentaje;
		double años;
		años = 7;
		double interes = principal * tasa * años;
		System.out.println("El interés simple: " + interes);
				
				
	
	
		

	}

}
