package _EjerciciosWhile;

public class _Ejercicio1 {
	//IMPRIMIR NÚMEROS DEL 1 AL 10

	public static void main(String[] args) {
		System.out.println("APLICACIÓN NUMERO DEL 1 AL 10");
		System.out.println("-----------------------------");
		System.out.println("-----------------------------");
		
		int numero = 1;
		while(numero <= 10) {
			System.out.println(numero++);
		}
	}

}
